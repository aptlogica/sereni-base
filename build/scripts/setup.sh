# Configure public host
configure_host() {
    echo -e "\n${BLUE}Network Configuration${NC}\n"
    
    # Detect local IP for display purposes
    local_ip=$(hostname -I 2>/dev/null | awk '{print $1}' || ipconfig getifaddr en0 2>/dev/null || echo "")
    
    if [ -n "$local_ip" ]; then
        echo "Detected local IP: $local_ip"
        echo ""
    fi
    
    echo "Enter your public IP address or domain name:"
    echo "(Examples: 192.168.1.100, myapp.example.com, or localhost for local development)"
    echo ""
    read -p "IP/Domain [localhost]: " PUBLIC_HOST
    
    # Use localhost as default if nothing entered
    if [ -z "$PUBLIC_HOST" ]; then
        PUBLIC_HOST="localhost"
    fi
    
    # Escape special characters for sed
    ESCAPED_HOST=$(printf '%s\n' "$PUBLIC_HOST" | sed -e 's/[&/\]/\\&/g')
    
    # Update .env file
    if [[ "$OSTYPE" == "darwin"* ]]; then
        sed -i '' "s/^PUBLIC_HOST=.*/PUBLIC_HOST=$ESCAPED_HOST/" .env
        sed -i '' "s|^BASEUI_VITE_API_BASE_URL=.*|BASEUI_VITE_API_BASE_URL=http://$ESCAPED_HOST:8080|" .env
    else
        sed -i "s/^PUBLIC_HOST=.*/PUBLIC_HOST=$ESCAPED_HOST/" .env
        sed -i "s|^BASEUI_VITE_API_BASE_URL=.*|BASEUI_VITE_API_BASE_URL=http://$ESCAPED_HOST:8080|" .env
    fi
    
    print_step "Configured PUBLIC_HOST=$PUBLIC_HOST"
    print_step "Configured BASEUI_VITE_API_BASE_URL=http://$PUBLIC_HOST:8080"
}
configure_owner() {
    echo -e "\n${BLUE}Owner Registration Configuration${NC}\n"
    
    echo "Enter owner registration details (press Enter to use defaults):"
    echo ""
    
    read -p "First Name [Admin]: " OWNER_FIRST_NAME
    if [ -z "$OWNER_FIRST_NAME" ]; then
        OWNER_FIRST_NAME="Admin"
    fi
    
    read -p "Last Name [User]: " OWNER_LAST_NAME
    if [ -z "$OWNER_LAST_NAME" ]; then
        OWNER_LAST_NAME="User"
    fi
    
    read -p "Email [admin@example.com]: " OWNER_EMAIL
    if [ -z "$OWNER_EMAIL" ]; then
        OWNER_EMAIL="admin@example.com"
    fi
    
    read -p "Password [Admin@123]: " OWNER_PASSWORD
    if [ -z "$OWNER_PASSWORD" ]; then
        OWNER_PASSWORD="Admin@123"
    fi
    
    # Escape special characters for sed (& / \ and newlines)
    escape_for_sed() {
        printf '%s\n' "$1" | sed -e 's/[&/\]/\\&/g'
    }
    
    ESCAPED_FIRST_NAME=$(escape_for_sed "$OWNER_FIRST_NAME")
    ESCAPED_LAST_NAME=$(escape_for_sed "$OWNER_LAST_NAME")
    ESCAPED_EMAIL=$(escape_for_sed "$OWNER_EMAIL")
    ESCAPED_PASSWORD=$(escape_for_sed "$OWNER_PASSWORD")
    
    # Update .env file using escaped values
    if [[ "$OSTYPE" == "darwin"* ]]; then
        sed -i '' "s/^OWNER_FIRST_NAME=.*/OWNER_FIRST_NAME=$ESCAPED_FIRST_NAME/" .env
        sed -i '' "s/^OWNER_LAST_NAME=.*/OWNER_LAST_NAME=$ESCAPED_LAST_NAME/" .env
        sed -i '' "s/^OWNER_EMAIL=.*/OWNER_EMAIL=$ESCAPED_EMAIL/" .env
        sed -i '' "s/^OWNER_PASSWORD=.*/OWNER_PASSWORD=$ESCAPED_PASSWORD/" .env
    else
        sed -i "s/^OWNER_FIRST_NAME=.*/OWNER_FIRST_NAME=$ESCAPED_FIRST_NAME/" .env
        sed -i "s/^OWNER_LAST_NAME=.*/OWNER_LAST_NAME=$ESCAPED_LAST_NAME/" .env
        sed -i "s/^OWNER_EMAIL=.*/OWNER_EMAIL=$ESCAPED_EMAIL/" .env
        sed -i "s/^OWNER_PASSWORD=.*/OWNER_PASSWORD=$ESCAPED_PASSWORD/" .env
    fi
    
    print_step "Owner configuration set"
}

# Clone repositories
clone_repos() {
    echo -e "\n${BLUE}Cloning repositories...${NC}\n"
    
    chmod +x build/scripts/*.sh 2>/dev/null || true
    
    if [ -f "build/scripts/clone-services.sh" ]; then
        bash build/scripts/clone-services.sh
        print_step "Cloned microservices"
    fi
    
    if [ -f "build/scripts/clone-go-postgres-rest.sh" ]; then
        bash build/scripts/clone-go-postgres-rest.sh
        print_step "Cloned go-postgres-rest"
    fi
}

# Start services
start_services() {
    echo -e "\n${BLUE}Starting services...${NC}\n"
    
    docker compose -f docker-compose.all.yaml up --build -d
    
    print_step "Services started!"
    
    echo -e "\n${BLUE}Waiting for services to be ready...${NC}"
    sleep 10
    
    docker compose -f docker-compose.all.yaml ps
}

# Print completion message
print_completion() {
    echo -e "\n${GREEN}"
    echo "========================================================================"
    echo "                      SETUP COMPLETE!"
    echo "========================================================================"
    echo -e "${NC}"
    
    echo -e "Access your application at:"
    echo -e "  ${GREEN}Frontend:${NC}  http://$PUBLIC_HOST:5050"
    echo -e "  ${GREEN}Backend:${NC}   http://$PUBLIC_HOST:8080"
    echo -e "  ${GREEN}MinIO:${NC}     http://$PUBLIC_HOST:9001"
    echo ""
    echo -e "Default admin credentials:"
    echo -e "  ${YELLOW}Email:${NC}    admin@example.com"
    echo -e "  ${YELLOW}Password:${NC} Admin@123"
    echo ""
    echo -e "${YELLOW}WARNING: Remember to change default passwords in production!${NC}"
    echo ""
    echo "Useful commands:"
    echo "  make logs      - View service logs"
    echo "  make down-all  - Stop all services"
    echo "  make clean     - Remove all data"
}

# Main execution
main() {
    print_header
    check_prerequisites
    setup_environment
    configure_host
    configure_owner
    clone_repos
    start_services
    print_completion
}

# Run main function
main
